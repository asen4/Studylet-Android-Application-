package com.example.studyletapplication;

public class Comments {

    private String comment, commentID, firstName, lastName, profileImage, date, time;

    public Comments() {

    }

    public Comments(String comment, String commentID, String firstName, String lastName, String profileImage, String date, String time) {
        this.comment = comment;
        this.commentID = commentID;
        this.firstName = firstName;
        this.lastName = lastName;
        this.profileImage = profileImage;
        this.date = date;
        this.time = time;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getCommentID() {
        return commentID;
    }

    public void setCommentID(String commentID) {
        this.commentID = commentID;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getProfileImage() {
        return profileImage;
    }

    public void setProfileImage(String profileImage) {
        this.profileImage = profileImage;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
